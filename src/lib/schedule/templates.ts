export interface ScheduleTemplate {
  id: string;
  name: string;
  description: string;
  workload: "light" | "moderate" | "intense";
  focusRatio: number;
  recoveryRatio: number;
  generate: () => ScheduleData;
}

function uid(prefix: string): string {
  return `${prefix}-${Math.random().toString(36).slice(2, 8)}`;
}

function rBlock(
  day: number,
  start: string,
  end: string,
  kind: string,
  title: string,
  endsNextDay?: boolean,
): RoutineBlock {
  return { id: uid("r"), day, start, end, kind, title, endsNextDay };
}

function sleepBlocks(prefix: string): RoutineBlock[] {
  const blocks: RoutineBlock[] = [];
  for (let d = 0; d < 7; d++) {
    blocks.push(rBlock(d, "00:00", "07:00", "sleep", `${prefix} Sleep`));
    blocks.push(rBlock(d, "22:30", "23:59", "sleep", `${prefix} Sleep`));
  }
  return blocks;
}

